async def seed_chemicals(db: AsyncSession) -> int:
    """Seed chemicals into database"""
    count = 0
    errors = []

    for chem_data in SAMPLE_CHEMICALS:
        try:
            # Check if chemical already exists
            existing = await db.execute(
                select(Chemical).where(Chemical.cas_number == chem_data["cas_number"])
            )
            if existing.scalar_one_or_none():
                print(f"  Skipping {chem_data['name']} (already exists)")
                continue

            # Create chemical
            chemical = Chemical(**chem_data)
            db.add(chemical)
            count += 1
            print(f"  ✓ Added {chem_data['name']}")

        except Exception as e:
            errors.append(f"{chem_data['name']}: {str(e)}")
            print(f"  ✗ Error adding {chem_data['name']}: {e}")

    # Commit all changes
    await db.commit()

    # Print summary
    print(f"\n{'='*60}")
    print(f"Seeding Complete!")
    print(f"{'='*60}")
    print(f"Chemicals added: {count}")
    if errors:
        print(f"\nErrors ({len(errors)}):")
        for error in errors:
            print(f"  - {error}")

    return count


async def main():
    """Main seeding function"""
    print(f"{'='*60}")
    print("ToxZone Chemical Database Seeding")
    print(f"{'='*60}\n")

    # Create engine
    engine = create_async_engine(
        settings.database_url,
        echo=False,
    )

    # Create session
    async_session_maker = async_sessionmaker(
        engine,
        class_=AsyncSession,
        expire_on_commit=False,
    )

    async with async_session_maker() as session:
        try:
            count = await seed_chemicals(session)
            print(f"\n✓ Successfully seeded {count} chemicals")
        except Exception as e:
            print(f"\n✗ Seeding failed: {e}")
            await session.rollback()
            raise
        finally:
            await engine.dispose()


if __name__ == "__main__":
    asyncio.run(main())